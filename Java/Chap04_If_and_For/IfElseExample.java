package sec01;

public class IfElseExample {
	public static void main(String[] args) {
		int score = 85;
		
		if(score>=90) {
			System.out.println("Score is over 90");
			System.out.println("Grade is A");
		} else {
			System.out.println("Score is under 90");
			System.out.println("Grade is B");
		}
	}
}
